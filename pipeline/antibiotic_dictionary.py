def build_antibiotic_dictionary(prescriptions_df, output_path=None):
    """Build antibiotic dictionary from raw PRESCRIPTIONS drug names.
    Uses built-in mapping for known drugs, flags unknown for manual review.
    """
    if output_path is None:
        output_path = ABX_DICT_CSV

    raw_names = prescriptions_df["drug_name"].dropna().unique()

    records = []
    for raw in raw_names:
        raw_clean = raw.strip()
        if raw_clean in DRUG_NAME_MAP:
            drug_std, route = DRUG_NAME_MAP[raw_clean]
            if drug_std is None:
                records.append({
                    "drug_raw": raw_clean,
                    "drug_std": None,
                    "is_antibiotic": 0,
                    "is_systemic_antibiotic": 0,
                    "route": route if route else "unknown",
                    "class": "non_antibiotic",
                    "spectrum_score": 0,
                    "aware_group": "not_applicable",
                    "anti_pseudomonal": 0, "anti_mrsa": 0,
                    "carbapenem": 0, "anti_anaerobe": 0,
                    "salvage_therapy": 0,
                    "glycopeptide_or_oxazolidinone": 0,
                    "coverage_gnb": 0, "coverage_pseudomonas": 0,
                    "coverage_mrsa": 0, "coverage_anaerobe": 0,
                    "coverage_atypical": 0, "coverage_fungal": 0,
                    "standard_dosing_interval_hours": None,
                    "needs_review": 0,
                })
                continue

            db_entry = ANTIBIOTIC_DB.get(drug_std)
            if db_entry:
                route = route if route else db_entry.get("route", "iv")
                is_systemic = 1 if route in ("iv", "oral", "im", "intramuscular",
                                              "intraosseous", "iv/oral") else 0
                is_broad = 1 if db_entry["spectrum_score"] >= 3 else 0

                records.append({
                    "drug_raw": raw_clean,
                    "drug_std": drug_std,
                    "is_antibiotic": 1,
                    "is_systemic_antibiotic": is_systemic,
                    "route": route,
                    "class": db_entry["class"],
                    "spectrum_score": db_entry["spectrum_score"],
                    "aware_group": db_entry.get("aware", ""),
                    "anti_pseudomonal": db_entry["anti_pseudomonal"],
                    "anti_mrsa": db_entry["anti_mrsa"],
                    "carbapenem": db_entry["carbapenem"],
                    "anti_anaerobe": db_entry["anti_anaerobe"],
                    "salvage_therapy": db_entry["salvage_therapy"],
                    "glycopeptide_or_oxazolidinone": 1 if drug_std in
                        ("万古霉素", "替考拉宁", "利奈唑胺") else 0,
                    "coverage_gnb": db_entry["coverage_gnb"],
                    "coverage_pseudomonas": db_entry["coverage_pseudomonas"],
                    "coverage_mrsa": db_entry["coverage_mrsa"],
                    "coverage_anaerobe": db_entry["coverage_anaerobe"],
                    "coverage_atypical": db_entry["coverage_atypical"],
                    "coverage_fungal": db_entry["coverage_fungal"],
                    "standard_dosing_interval_hours": db_entry["dose_interval"],
                    "needs_review": 0,
                })
        else:
            # Unknown drug - flag for manual review
            records.append({
                "drug_raw": raw_clean,
                "drug_std": None, "is_antibiotic": 0, "is_systemic_antibiotic": 0,
                "route": "unknown", "class": "unknown", "spectrum_score": 0,
                "aware_group": "", "anti_pseudomonal": 0, "anti_mrsa": 0,
                "carbapenem": 0, "anti_anaerobe": 0, "salvage_therapy": 0,
                "glycopeptide_or_oxazolidinone": 0,
                "coverage_gnb": 0, "coverage_pseudomonas": 0,
                "coverage_mrsa": 0, "coverage_anaerobe": 0,
                "coverage_atypical": 0, "coverage_fungal": 0,
                "standard_dosing_interval_hours": None,
                "needs_review": 1,
            })

    df = pd.DataFrame(records)
    df.to_csv(output_path, index=False, encoding="utf-8")
    return df


def load_antibiotic_dictionary(path=ABX_DICT_CSV):
    """Load the antibiotic dictionary from CSV."""
    return pd.read_csv(path)
